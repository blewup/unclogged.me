/**
 * Get shared SVG icon by name with optional class and size
 * @param {string} name - Icon name from SHARED_ICONS
 * @param {string} className - CSS classes to add
 * @param {string} size - Size (e.g., 'w-5 h-5')
 * @returns {string} SVG HTML string
 */
function getSharedIcon(name, className = '', size = 'w-5 h-5') {
    const icon = SHARED_ICONS[name];
    if (!icon) {
        console.warn(`Shared icon "${name}" not found`);
        return '';
    }
    return icon.replace('<svg', `<svg class="${size} ${className}"`);
}

/**
 * Inject shared icon into element
 * @param {string} elementId - Target element ID
 * @param {string} iconName - Icon name
 * @param {string} size - Size classes
 */
function injectSharedIcon(elementId, iconName, size = 'w-5 h-5') {
    const element = document.getElementById(elementId);
    if (element) {
        element.innerHTML = getSharedIcon(iconName, '', size);
    }
}

